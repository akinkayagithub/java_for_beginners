package print_statements;

public class PrintVsPrintln {
    public static void main(String[] args) {

        //println
        System.out.println("J");
        System.out.println("A");
        System.out.println("V");
        System.out.println("A");

        //print
        System.out.print("J");
        System.out.print("A");
        System.out.print("V");
        System.out.print("A");
    }
}
