package concatenation;

public class PracticeConcatenation {
    public static void main(String[] args) {

        System.out.println("Hello World"); // Hello World
        System.out.println("Hello " + "World"); // Hello World
        System.out.println("Hello" + " World"); // Hello World
        System.out.println("Hello" + " " + "World"); // Hello World

        System.out.println("I practice Java");
        System.out.println("I" + " " + "practice" + " " + "Java");
    }
}
